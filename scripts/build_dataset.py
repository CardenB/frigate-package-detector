#!/usr/bin/env python3
"""Merge data/interim/* into a single train/val/test set under data/final/.

Splitting is stratified PER SOURCE so package-heavy sources are represented in
val/test (a global shuffle would let big COCO/OpenImages dominate val). Writes
data/final/data.yaml ready for Ultralytics.
"""
from __future__ import annotations

import os
import random
import shutil
from pathlib import Path

from common import FINAL, INTERIM, REPO, class_index, datasets_cfg, unified_classes

VAL_FRAC = float(os.environ.get("VAL_FRAC", "0.1"))
TEST_FRAC = float(os.environ.get("TEST_FRAC", "0.05"))
SEED = int(os.environ.get("SPLIT_SEED", "0"))


def link(src: Path, dst: Path) -> None:
    if dst.exists() or dst.is_symlink():
        return
    try:
        dst.symlink_to(src.resolve())
    except OSError:
        shutil.copy2(src, dst)


def main() -> None:
    rng = random.Random(SEED)

    for split in ("train", "val", "test"):
        for kind in ("images", "labels"):
            d = FINAL / split / kind
            if d.exists():
                shutil.rmtree(d)
            d.mkdir(parents=True, exist_ok=True)

    sources = [p for p in sorted(INTERIM.iterdir()) if (p / "images").is_dir()] \
        if INTERIM.exists() else []
    if not sources:
        raise SystemExit("No interim data. Run convert_to_yolo.py first.")

    counts = {"train": 0, "val": 0, "test": 0}
    for src in sources:
        imgs = sorted((src / "images").iterdir())
        rng.shuffle(imgs)
        n = len(imgs)
        n_val = int(n * VAL_FRAC)
        n_test = int(n * TEST_FRAC)
        buckets = {
            "val": imgs[:n_val],
            "test": imgs[n_val:n_val + n_test],
            "train": imgs[n_val + n_test:],
        }
        # Per-source oversample (train only): duplicate in-domain sources so they
        # aren't drowned out by the big generic sets. Never duplicate val/test.
        ovs = int(datasets_cfg().get(src.name, {}).get("oversample", 1) or 1)
        # Class-presence oversample (train only): duplicate any image containing a
        # targeted rare class (e.g. package) WHEREVER it appears, without
        # inflating other classes in big mixed sources. The two factors combine
        # via max() so a frigate(x8) package frame isn't multiplied to 8*N.
        cls_ovs = datasets_cfg().get("oversample_classes", {}) or {}
        cidx = class_index()
        cls_ovs_ids = {str(cidx[k]): int(v) for k, v in cls_ovs.items() if k in cidx}

        src_counts = {"train": 0, "val": 0, "test": 0}
        for split, items in buckets.items():
            for img in items:
                lbl = src / "labels" / (img.stem + ".txt")
                if not lbl.exists():
                    continue  # image with no label file at all
                reps = 1
                if split == "train":
                    reps = ovs
                    if cls_ovs_ids:
                        ids = {ln.split()[0] for ln in lbl.read_text().splitlines() if ln.strip()}
                        hits = [n for i, n in cls_ovs_ids.items() if i in ids]
                        if hits:
                            reps = max(reps, max(hits))
                for k in range(reps):
                    # __osK suffix marks duplicates so analyze_balance.py can
                    # separate originals (before) from oversampled copies (after).
                    sfx = "" if k == 0 else f"__os{k}"
                    link(img, FINAL / split / "images" / f"{img.stem}{sfx}{img.suffix}")
                    link(lbl, FINAL / split / "labels" / f"{img.stem}{sfx}.txt")
                    counts[split] += 1
                    src_counts[split] += 1
        tags = []
        if ovs > 1:
            tags.append(f"src x{ovs}")
        if cls_ovs_ids:
            tags.append("class-ovs")
        tag = f" [{', '.join(tags)}]" if tags else ""
        print(f"[build] {src.name}: {n} imgs{tag} -> "
              f"train {src_counts['train']}, val {src_counts['val']}, test {src_counts['test']}")

    names = unified_classes()
    data_yaml = FINAL / "data.yaml"
    lines = [
        f"# Auto-generated by build_dataset.py — do not hand-edit.",
        f"path: {FINAL}",
        "train: train/images",
        "val: val/images",
        "test: test/images",
        f"nc: {len(names)}",
        "names:",
    ]
    lines += [f"  {i}: {n}" for i, n in enumerate(names)]
    data_yaml.write_text("\n".join(lines) + "\n")

    print(f"[build] totals: {counts}")
    print(f"[build] wrote {data_yaml}")
    print(f"[build] classes: {names}")


if __name__ == "__main__":
    main()
